package br.unesp.igce.gerenciador_mangas_HQs;

/**
 *
 * @author aluno
 */
public class Fasciculo_Manga {
    private int volume;
    private int tipo;
    private int idioma;

    public Fasciculo_Manga() {
    }

    
    
    public int getIdioma() {
        return idioma;
    }

    public int getTipo() {
        return tipo;
    }

    public int getVolume() {
        return volume;
    }

    public void setIdioma(int idioma) {
        this.idioma = idioma;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }

    public void setVolume(int volume) {
        this.volume = volume;
    }

    
    
    
}
